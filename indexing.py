name = "akash it"

# Indexing:accessing characters using position
print(name[0])     # Output: a   (first character)
print(name[2])     # Output: a   (third character, index starts from 0)
print(name[-1])    # Output: t   (last character using negative index)
print(name[0:5])   # Output: akash   (slicing from index 0 to 4)
print(name[:5])    # Output: akash   (same as above, start is default 0)
print(name[6:])    # Output: it      (from index 6 till end)
print(name[::-1])  # Output: ti hsaka   (string reversed)
